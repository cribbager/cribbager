//go:build ignore

// This program regenerates keepprob.go: the per-rank probability that a dealt
// card is KEPT (not thrown to the crib) under the champion's own discard policy
// — the prior behind the calibrated pegging opponent model. If our discard
// policy is near-optimal, a rational opponent's throws look like ours, so the
// bot's belief about which unseen cards sit in the opponent's hand should be
// shaped by this table instead of a uniform draw (the classic example: a 5 is
// almost never thrown to the opponent's crib, so an unseen 5 is more likely in
// their hand than a uniform model says).
//
// Exhaustively sweeping all C(52,6) ≈ 20.4M hands through RankDiscards is too
// slow, so this samples hands from a fixed-seed RNG — regeneration is
// reproducible, and at the default sample size each rank is dealt >400k times,
// putting the standard error under 0.001.
//
// Not part of the package build; regenerate with:
//
//	go generate ./internal/bot/eval
package main

import (
	"bytes"
	"fmt"
	"go/format"
	"math/rand"
	"os"
	"time"

	"github.com/cribbager/cribbager/internal/bot/eval"
	"github.com/cribbager/cribbager/internal/cribbage"
)

const (
	outPath = "keepprob.go"
	seed    = 1
	hands   = 1_000_000
)

func main() {
	rng := rand.New(rand.NewSource(seed))
	deck := cribbage.Deck()

	// dealt[rank] counts cards dealt; kept[role][rank] counts cards kept, where
	// role 0 = the discarder was pone, role 1 = the discarder was dealer.
	var dealt [14]int
	var kept [2][14]int

	start := time.Now()
	for n := 0; n < hands; n++ {
		var h [6]cribbage.Card
		for i, idx := range rng.Perm(52)[:6] {
			h[i] = deck[idx]
		}
		for _, c := range h {
			dealt[c.Rank]++
		}
		for role, myCrib := range []bool{false, true} {
			top := eval.RankDiscards(h, myCrib)[0]
			for _, c := range top.Keep {
				kept[role][c.Rank]++
			}
		}
		if n%50_000 == 0 {
			fmt.Fprintf(os.Stderr, "\r  sampling… %d/%d (%.0fs)", n, hands, time.Since(start).Seconds())
		}
	}
	fmt.Fprintln(os.Stderr)

	var buf bytes.Buffer
	buf.WriteString("// Code generated by go generate (keepprob_gen.go); DO NOT EDIT.\n\n")
	buf.WriteString("package eval\n\n")
	fmt.Fprintf(&buf, "// keepProb[role][rank] is the probability that a dealt card of this rank is\n")
	fmt.Fprintf(&buf, "// kept in the 4-card hand under the champion's discard policy, estimated from\n")
	fmt.Fprintf(&buf, "// %d seeded random hands (seed %d). role 0: the discarder was the pone\n", hands, seed)
	fmt.Fprintf(&buf, "// (throwing to the opponent's crib); role 1: the discarder was the dealer\n")
	fmt.Fprintf(&buf, "// (throwing to their own crib). See keepprob_gen.go.\n")
	buf.WriteString("var keepProb = [2][14]float64{\n")
	for role := 0; role < 2; role++ {
		buf.WriteString("\t{0")
		for r := 1; r <= 13; r++ {
			fmt.Fprintf(&buf, ", %.4f", float64(kept[role][r])/float64(dealt[r]))
		}
		buf.WriteString("},\n")
	}
	buf.WriteString("}\n")

	src, err := format.Source(buf.Bytes())
	if err != nil {
		fmt.Fprintf(os.Stderr, "gofmt: %v\n", err)
		os.Exit(1)
	}
	if err := os.WriteFile(outPath, src, 0o644); err != nil {
		fmt.Fprintf(os.Stderr, "write %s: %v\n", outPath, err)
		os.Exit(1)
	}
	fmt.Printf("wrote %s\n", outPath)
}
