//go:build ignore

// This program regenerates cribtable.go: the exact expected crib value (in
// points) contributed by discarding two cards, averaged over a uniform opponent
// (the two other crib cards) and the starter, drawn from the 50 cards that are
// not the two discards. This is the standard "discard to crib" expectation under
// a uniform opponent model — deterministic and exact, so the discard evaluator
// needs no runtime sampling.
//
// The value depends only on the two discards' ranks and whether they share a
// suit (suit identities are interchangeable by symmetry), so the table is
// indexed [loRank][hiRank][suited]. Pairs (loRank == hiRank) are always
// different suits, so only the unsuited slot is used.
//
// It is not part of the package build (the `ignore` tag excludes it) and ships
// in no binary. Regenerate the table with:
//
//	go generate ./internal/bot/eval
package main

import (
	"bytes"
	"fmt"
	"go/format"
	"math"
	"os"

	"github.com/cribbager/cribbager/internal/cribbage"
	"github.com/cribbager/cribbager/internal/scoring/hand"
)

const outPath = "cribtable.go"

func remainingAfter(a, b cribbage.Card) []cribbage.Card {
	out := make([]cribbage.Card, 0, 50)
	for _, c := range cribbage.Deck() {
		if c == a || c == b {
			continue
		}
		out = append(out, c)
	}
	return out
}

// expectedCrib enumerates every completion of the crib — the starter plus the
// opponent's two cards, drawn from the 50 cards not discarded — and returns the
// mean crib score and its full distribution (histogram over 0..29) across all
// of them. The distribution feeds the win-probability objective, which needs
// more than the mean near the end of the game.
func expectedCrib(a, b cribbage.Card) (float64, [30]float64) {
	rest := remainingAfter(a, b)
	n := len(rest)
	var dist [30]float64
	sum, count := 0, 0
	for s := 0; s < n; s++ {
		starter := rest[s]
		for i := 0; i < n; i++ {
			if i == s {
				continue
			}
			for j := i + 1; j < n; j++ {
				if j == s {
					continue
				}
				crib := [4]cribbage.Card{a, b, rest[i], rest[j]}
				t, _ := hand.Total(crib, starter, true)
				sum += t
				dist[t]++
				count++
			}
		}
	}
	for i := range dist {
		dist[i] /= float64(count)
	}
	return float64(sum) / float64(count), dist
}

func round4(x float64) float64 { return math.Round(x*10000) / 10000 }

func card(rank, suit int) cribbage.Card {
	return cribbage.Card{Rank: cribbage.Rank(rank), Suit: cribbage.Suit(suit)}
}

func main() {
	var buf bytes.Buffer
	buf.WriteString("// Code generated by go generate (cribtable_gen.go); DO NOT EDIT.\n\n")
	buf.WriteString("package eval\n\n")
	buf.WriteString("// cribEVTable[lo][hi][suited] is the exact expected crib value of discarding\n")
	buf.WriteString("// two cards of ranks lo<=hi (1..13), where suited is 1 when they share a suit.\n")
	buf.WriteString("// Pairs use only the unsuited (0) slot. See cribtable_gen.go.\n")
	buf.WriteString("var cribEVTable = [14][14][2]float64{}\n\n")
	buf.WriteString("// cribDistTable holds the matching exact crib score DISTRIBUTIONS (histogram\n")
	buf.WriteString("// over scores 0..29), for the win-probability objective. Same indexing.\n")
	buf.WriteString("var cribDistTable = [14][14][2][30]float32{}\n\n")
	buf.WriteString("func init() {\n")

	emit := func(lo, hi, suited int, v float64, dist [30]float64) {
		fmt.Fprintf(&buf, "\tcribEVTable[%d][%d][%d] = %g\n", lo, hi, suited, round4(v))
		fmt.Fprintf(&buf, "\tcribDistTable[%d][%d][%d] = [30]float32{", lo, hi, suited)
		last := 0
		for i, p := range dist {
			if p > 0 {
				last = i
			}
		}
		for i := 0; i <= last; i++ {
			if i > 0 {
				buf.WriteString(", ")
			}
			fmt.Fprintf(&buf, "%g", round4(dist[i]))
		}
		buf.WriteString("}\n")
	}

	for lo := 1; lo <= 13; lo++ {
		for hi := lo; hi <= 13; hi++ {
			if lo == hi {
				v, d := expectedCrib(card(lo, 0), card(lo, 1))
				emit(lo, hi, 0, v, d)
			} else {
				u, ud := expectedCrib(card(lo, 0), card(hi, 1))
				s, sd := expectedCrib(card(lo, 0), card(hi, 0))
				emit(lo, hi, 0, u, ud)
				emit(lo, hi, 1, s, sd)
			}
		}
		fmt.Fprintf(os.Stderr, "\r  generating crib table… rank %d/13", lo)
	}
	fmt.Fprintln(os.Stderr)
	buf.WriteString("}\n")

	src, err := format.Source(buf.Bytes())
	if err != nil {
		fmt.Fprintf(os.Stderr, "gofmt: %v\n", err)
		os.Exit(1)
	}
	if err := os.WriteFile(outPath, src, 0o644); err != nil {
		fmt.Fprintf(os.Stderr, "write %s: %v\n", outPath, err)
		os.Exit(1)
	}
	fmt.Printf("wrote %s\n", outPath)
}
